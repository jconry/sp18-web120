    <h2>Galleries</h2>
        <p>Adding an image gallery to a website is a key component of the design process, especially if the site is focused on art, hospitality, travel and any other site that has a lot of visual focus. There are so many options available which makes it difficult to know where to start. A good starting point is to do an internet search for online photo galleries then view the options that are available to see what is the best fit for the website you’re working on. An important factor in the decision making process is what type of features does your client want. Do they want a slideshow or multi page gallery or a pop-out gallery. 

	<p>The majority galleries use Jquery or JavaScript, but there are also other options that rely just on CSS. A good CSS option is <a href="https://www.cssslider.com">CSS Slider</a>. <a href="https://www.mobirise.com/bootstrap-gallery/">Mobirise Gallery</a>,  offers a variety of image galleries including one that uses CSS.</p> 

<p>Of course there are many image gallery options that rely on Jquery and JavaScript. Here are a few standouts: 
    <a href="https://www.galleria.io">Galleria</a>, 
    <a href="https://www.juicebox">Juicebox</a>, 
    <a href="https://www.highslide.com">HighSlide</a>, 
    <a href="https://www.bqworks.com/accordian-slider/">Accordian Slider </a>, 
    <a href="https://www.fotorama.io">Fotorama</a> and 
    <a href="https://www.host.sonspring.com/hoverbox/">Hoverbox</a>.</p>

	<p>There are also some fun options out there. Simpleviewer uses flash to mimic postcards being shuffled on a surface <a href="https://www.simpleviewer.net/postcardviewer/">Simple Viewer</a>. Scattered Polaroids displays your photos are polaroids scattered on a surface <a href="https://www.tympanus.net/codrops/2014/01/28/scattered-polaroids-gallery/">Scattered Polaroids</a>. </p>

