    <h2>Parallax</h2>
        <p>Parallax has been popular in the design world for awhile now. It was originally introduced in computer games in the 1980’s. It’s a fun, modern visual effect to add to your web design toolkit. Parallax scrolling is the action of elements moving independently. On a webpage the background image moves at a different speed than the foreground content.</p>
        <p>There are multiple ways to implement parallax in your designs. </p>
    <ul>
        <li>Raster Parallax gives an optical illusion of movement.</li>
        <li>Repeat Pattern uses scrolling over a repeating background.</li>
        <li>Layered Parallax is when the foreground and background elements move at a different speed.
</li>
        <li>Sprite Method which is layers of individually controllable moving objects.</li>
</ul>
        <p>While adding parallax to your designs can be an interesting way to mix things up, it also has its downsides. The biggest drawback is that it is difficult to make work with responsive design and since responsive is crucial these days that may be the determining factor when deciding to use parallax or not on a website.  Another downside is it makes it more difficult to diversify meta information which can have an negative effect on your SEO. Parallax scrolling can also affect the load time, especially for users with slower internet connections.</p>
        <p>Ultimately when choosing to add parallax scrolling to a website or not depends on the type of business as well as the look, feel and experience the client is going for. The majority of sites do not use parallax so adding it to at minimum the homepage can make the site stand out in a world of websites. 
</p>
